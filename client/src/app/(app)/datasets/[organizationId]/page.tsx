'use client';

import { JSX, useCallback, useEffect, useMemo, useState } from 'react';
import Link from 'next/link';
import { usePathname, useRouter, useSearchParams } from 'next/navigation';
import { useMutation, useQuery, useQueryClient } from '@tanstack/react-query';
import { Plus, RefreshCw, Search } from 'lucide-react';

import { ErrorPanel } from '@/components/ErrorPanel';
import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import {
  Dialog,
  DialogContent,
  DialogFooter,
  DialogHeader,
  DialogTitle,
} from '@/components/ui/dialog';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { Select } from '@/components/ui/select';
import { Textarea } from '@/components/ui/textarea';
import { useWorkspaceScope } from '@/context/workspaceScope';
import { createDisplayError, toDisplayError, type DisplayError } from '@/lib/errors';
import { fetchConnectors } from '@/orchestration/connectors';
import {
  createDataset,
  listDatasets,
  type DatasetColumn,
  type DatasetCreatePayload,
  type DatasetType,
  uploadCsvDataset,
} from '@/orchestration/datasets';
import { fetchConnectorColumns, fetchConnectorSchemas, fetchConnectorTables } from '@/orchestration/sql';

type DatasetsPageProps = {
  params: { organizationId: string };
};

type CreateState = {
  name: string;
  description: string;
  tags: string;
  datasetType: DatasetType;
  file: File | null;
  connectionId: string;
  schemaName: string;
  tableName: string;
  sqlText: string;
  dialect: string;
  maxRowsPreview: string;
};

const DEFAULT_CREATE_STATE: CreateState = {
  name: '',
  description: '',
  tags: '',
  datasetType: 'TABLE',
  file: null,
  connectionId: '',
  schemaName: '',
  tableName: '',
  sqlText: '',
  dialect: 'tsql',
  maxRowsPreview: '1000',
};

function isSqlConnector(connectorFamily?: string | null): boolean {
  if (!connectorFamily) {
    return false;
  }
  return connectorFamily.toUpperCase() == "DATABASE";
}

export default function DatasetsPage({ params }: DatasetsPageProps): JSX.Element {
  const organizationId = params.organizationId;
  const pathname = usePathname();
  const router = useRouter();
  const searchParams = useSearchParams();
  const queryClient = useQueryClient();
  const { selectedOrganizationId, selectedProjectId, setSelectedOrganizationId } = useWorkspaceScope();

  const [search, setSearch] = useState('');
  const [typeFilter, setTypeFilter] = useState<'all' | DatasetType>('all');
  const [autoGeneratedOnly, setAutoGeneratedOnly] = useState(
    searchParams.get('auto_generated') === '1',
  );
  const [createOpen, setCreateOpen] = useState(false);
  const [createState, setCreateState] = useState<CreateState>(DEFAULT_CREATE_STATE);
  const [schemas, setSchemas] = useState<string[]>([]);
  const [tables, setTables] = useState<string[]>([]);
  const [columns, setColumns] = useState<Array<{ name: string; type: string }>>([]);
  const [selectedColumns, setSelectedColumns] = useState<Record<string, boolean>>({});
  const [modalError, setModalError] = useState<DisplayError | null>(null);
  const [loadingSchemas, setLoadingSchemas] = useState(false);
  const [loadingTables, setLoadingTables] = useState(false);
  const [loadingColumns, setLoadingColumns] = useState(false);

  useEffect(() => {
    if (organizationId && organizationId !== selectedOrganizationId) {
      setSelectedOrganizationId(organizationId);
    }
  }, [organizationId, selectedOrganizationId, setSelectedOrganizationId]);

  const connectorsQuery = useQuery({
    queryKey: ['datasets-connectors', organizationId],
    queryFn: () => fetchConnectors(organizationId),
    enabled: Boolean(organizationId),
  });

  const datasetsQuery = useQuery({
    queryKey: ['datasets-list', organizationId, selectedProjectId, search, typeFilter, autoGeneratedOnly],
    queryFn: () =>
      listDatasets(organizationId, {
        projectId: selectedProjectId || undefined,
        search: search.trim() || undefined,
        tags: autoGeneratedOnly ? ['auto-generated'] : [],
        datasetTypes: typeFilter === 'all' ? [] : [typeFilter],
      }),
    enabled: Boolean(organizationId),
  });

  const createMutation = useMutation({
    mutationFn: async (payload: DatasetCreatePayload) => createDataset(payload),
    onSuccess: async (dataset) => {
      setCreateOpen(false);
      setCreateState(DEFAULT_CREATE_STATE);
      setColumns([]);
      setSchemas([]);
      setTables([]);
      setSelectedColumns({});
      if (pathname && searchParams.get('create') === '1') {
        router.replace(pathname, { scroll: false });
      }
      await queryClient.invalidateQueries({ queryKey: ['datasets-list', organizationId] });
      router.push(`/datasets/${organizationId}/${dataset.id}`);
    },
    onError: (error: unknown) => {
      setModalError(toDisplayError(error, 'dataset.form'));
    },
  });
  const uploadMutation = useMutation({
    mutationFn: async (payload: {
      workspaceId: string;
      name: string;
      file: File;
      projectId?: string | null;
      description?: string | null;
      tags?: string[];
    }) => uploadCsvDataset(payload),
    onSuccess: async (response) => {
      setCreateOpen(false);
      setCreateState(DEFAULT_CREATE_STATE);
      setColumns([]);
      setSchemas([]);
      setTables([]);
      setSelectedColumns({});
      if (pathname && searchParams.get('create') === '1') {
        router.replace(pathname, { scroll: false });
      }
      await queryClient.invalidateQueries({ queryKey: ['datasets-list', organizationId] });
      router.push(`/datasets/${organizationId}/${response.datasetId}`);
    },
    onError: (error: unknown) => {
      setModalError(toDisplayError(error, 'dataset.form'));
    },
  });

  const connectors = useMemo(() => connectorsQuery.data || [], [connectorsQuery.data]);

  const selectedColumnCount = useMemo(
    () => Object.values(selectedColumns).filter(Boolean).length,
    [selectedColumns],
  );
  const fieldErrors = modalError?.fieldErrors ?? {};

  const tableConnectors = useMemo(
    () => connectors.filter((connector) => isSqlConnector(connector.pluginMetadata?.connectorFamily)),
    [connectors],
  );

  const availableCreateConnectors = createState.datasetType === 'TABLE' ? tableConnectors : connectors;

  const loadSchemas = useCallback(
    async (connectionId: string) => {
      if (!connectionId) {
        setSchemas([]);
        return;
      }
      setLoadingSchemas(true);
      setModalError(null);
      try {
        const response = await fetchConnectorSchemas(organizationId, connectionId);
        setSchemas(response.schemas || []);
      } catch (error) {
        setModalError(toDisplayError(error, 'schema.browser'));
      } finally {
        setLoadingSchemas(false);
      }
    },
    [organizationId],
  );

  const loadTables = useCallback(
    async (connectionId: string, schemaName: string) => {
      if (!connectionId || !schemaName) {
        setTables([]);
        return;
      }
      setLoadingTables(true);
      setModalError(null);
      try {
        const response = await fetchConnectorTables(organizationId, connectionId, schemaName);
        setTables(response.tables || []);
      } catch (error) {
        setModalError(toDisplayError(error, 'schema.browser'));
      } finally {
        setLoadingTables(false);
      }
    },
    [organizationId],
  );

  const loadColumns = useCallback(
    async (connectionId: string, schemaName: string, tableName: string) => {
      if (!connectionId || !schemaName || !tableName) {
        setColumns([]);
        return;
      }
      setLoadingColumns(true);
      setModalError(null);
      try {
        const response = await fetchConnectorColumns(organizationId, connectionId, schemaName, tableName);
        const nextColumns = Object.values(response.columns || {});
        setColumns(nextColumns);
        const nextSelected: Record<string, boolean> = {};
        nextColumns.forEach((column) => {
          nextSelected[column.name] = true;
        });
        setSelectedColumns(nextSelected);
      } catch (error) {
        setModalError(toDisplayError(error, 'schema.browser'));
      } finally {
        setLoadingColumns(false);
      }
    },
    [organizationId],
  );

  useEffect(() => {
    if (createState.datasetType !== 'TABLE') {
      return;
    }
    const currentConnectorIsValid = tableConnectors.some((connector) => connector.id === createState.connectionId);
    if (currentConnectorIsValid) {
      return;
    }
    const nextConnectionId = tableConnectors[0]?.id || '';
        setCreateState((current) => ({
          ...current,
          connectionId: nextConnectionId,
          schemaName: '',
          tableName: '',
    }));
    setSchemas([]);
    setTables([]);
    setColumns([]);
    setSelectedColumns({});
    if (nextConnectionId) {
      void loadSchemas(nextConnectionId);
    }
  }, [createState.connectionId, createState.datasetType, loadSchemas, tableConnectors]);

  const openCreateModal = useCallback(() => {
    const defaultConnectionId = tableConnectors[0]?.id || connectors[0]?.id || '';
    setCreateState({
      ...DEFAULT_CREATE_STATE,
      connectionId: defaultConnectionId,
    });
    setModalError(null);
    setCreateOpen(true);
    if (defaultConnectionId) {
      void loadSchemas(defaultConnectionId);
    }
  }, [connectors, loadSchemas, tableConnectors]);

  useEffect(() => {
    if (searchParams.get('create') === '1' && !createOpen) {
      openCreateModal();
    }
  }, [createOpen, openCreateModal, searchParams]);

  const closeCreateModal = useCallback(() => {
    setCreateOpen(false);
    setModalError(null);
    if (pathname && searchParams.get('create') === '1') {
      router.replace(pathname, { scroll: false });
    }
  }, [pathname, router, searchParams]);

  const handleCreateOpenChange = useCallback(
    (open: boolean) => {
      if (!open) {
        closeCreateModal();
        return;
      }
      setCreateOpen(true);
    },
    [closeCreateModal],
  );

  async function submitCreate() {
    if (!organizationId) {
      return;
    }
    const tags = createState.tags
      .split(',')
      .map((item) => item.trim())
      .filter(Boolean);

    const payload: DatasetCreatePayload = {
      workspaceId: organizationId,
      projectId: selectedProjectId || undefined,
      name: createState.name.trim(),
      description: createState.description.trim() || undefined,
      tags,
      datasetType: createState.datasetType,
      connectionId: createState.connectionId || undefined,
      schemaName: createState.datasetType === 'TABLE' ? createState.schemaName || undefined : undefined,
      tableName: createState.datasetType === 'TABLE' ? createState.tableName || undefined : undefined,
      sqlText: createState.datasetType === 'SQL' ? createState.sqlText : undefined,
      dialect: createState.datasetType === 'SQL' ? createState.dialect : undefined,
      columns:
        createState.datasetType === 'TABLE'
          ? columns
              .filter((column) => selectedColumns[column.name])
              .map<DatasetColumn>((column, index) => ({
                name: column.name,
                dataType: column.type || 'unknown',
                nullable: true,
                isAllowed: true,
                isComputed: false,
                ordinalPosition: index,
              }))
          : [],
      policy: {
        maxRowsPreview: Math.max(1, Number(createState.maxRowsPreview) || 1000),
      },
      status: 'published',
    };

    if (!payload.name) {
      setModalError(
        createDisplayError({
          title: 'Dataset creation failed',
          message: 'Dataset name is required before Langbridge can create the asset.',
          suggestions: ['Provide a dataset name and try again.'],
          fieldErrors: { name: 'Dataset name is required.' },
          tone: 'warning',
        }),
      );
      return;
    }

    if (payload.datasetType === 'TABLE') {
      if (!payload.connectionId || !payload.tableName) {
        setModalError(
          createDisplayError({
            title: 'Dataset creation failed',
            message: 'Choose a connection, schema, and table before creating a table-backed dataset.',
            suggestions: ['Select a connection, schema, and table, then try again.'],
            fieldErrors: {
              connectionId: 'A connection is required.',
              schemaName: 'A schema is required.',
              tableName: 'A table is required.',
            },
            tone: 'warning',
          }),
        );
        return;
      }
    } else if (payload.datasetType === 'SQL' && !payload.sqlText?.trim()) {
      setModalError(
        createDisplayError({
          title: 'Dataset creation failed',
          message: 'SQL text is required for SQL-backed datasets.',
          suggestions: ['Add a SQL statement before creating the dataset.'],
          fieldErrors: { sqlText: 'SQL text is required.' },
          tone: 'warning',
        }),
      );
      return;
    }

    if (payload.datasetType === 'FILE') {
      if (!createState.file) {
        setModalError(
          createDisplayError({
            title: 'Dataset creation failed',
            message: 'Choose a CSV file before creating a file-backed dataset.',
            suggestions: ['Select a CSV file and try again.'],
            fieldErrors: { file: 'A CSV file is required.' },
            tone: 'warning',
          }),
        );
        return;
      }
      await uploadMutation.mutateAsync({
        workspaceId: organizationId,
        projectId: selectedProjectId || undefined,
        name: payload.name,
        description: payload.description,
        tags,
        file: createState.file,
      });
      return;
    }

    await createMutation.mutateAsync(payload);
  }

  const datasets = datasetsQuery.data?.items || [];

  return (
    <div className="space-y-6 text-[color:var(--text-secondary)]">
      <header className="surface-panel rounded-3xl p-6 shadow-soft">
        <div className="flex flex-wrap items-end justify-between gap-4">
          <div className="space-y-2">
            <p className="text-xs font-semibold uppercase tracking-[0.2em] text-[color:var(--text-muted)]">Datasets</p>
            <h1 className="text-2xl font-semibold text-[color:var(--text-primary)] md:text-3xl">Governed virtual datasets</h1>
            <p className="text-sm md:text-base">
              Build technical dataset abstractions over tables and SQL before binding semantic models.
            </p>
          </div>
          <Button onClick={openCreateModal} className="gap-2">
            <Plus className="h-4 w-4" /> New dataset
          </Button>
        </div>
      </header>

      <section className="surface-panel rounded-3xl p-6 shadow-soft space-y-4">
        <div className="flex flex-wrap items-center gap-3">
          <div className="relative min-w-[260px] flex-1">
            <Search className="pointer-events-none absolute left-3 top-3 h-4 w-4 text-[color:var(--text-muted)]" />
            <Input
              value={search}
              onChange={(event) => setSearch(event.target.value)}
              placeholder="Search datasets"
              className="pl-9"
            />
          </div>
          <Select value={typeFilter} onChange={(event) => setTypeFilter((event.target.value as 'all' | DatasetType) || 'all')} className="max-w-[180px]">
            <option value="all">All types</option>
            <option value="TABLE">Table</option>
            <option value="SQL">SQL</option>
            <option value="FEDERATED">Federated</option>
            <option value="FILE">File</option>
          </Select>
          <label className="flex items-center gap-2 text-xs">
            <input
              type="checkbox"
              checked={autoGeneratedOnly}
              onChange={(event) => setAutoGeneratedOnly(event.target.checked)}
            />
            Auto-generated
          </label>
          <Button variant="ghost" size="sm" onClick={() => datasetsQuery.refetch()}>
            <RefreshCw className="mr-2 h-4 w-4" /> Refresh
          </Button>
        </div>

        {datasetsQuery.isLoading ? <p className="text-sm">Loading datasets...</p> : null}
        {datasetsQuery.isError ? (
          <ErrorPanel {...toDisplayError(datasetsQuery.error)} />
        ) : null}
        {!datasetsQuery.isLoading && !datasetsQuery.isError && datasets.length === 0 ? (
          <div className="rounded-2xl border border-dashed border-[color:var(--panel-border)] bg-[color:var(--panel-alt)] p-6 text-sm">
            No datasets found for this scope.
          </div>
        ) : null}

        <ul className="space-y-3">
          {datasets.map((dataset) => (
            <li key={dataset.id}>
              <Link
                href={`/datasets/${organizationId}/${dataset.id}`}
                className="block rounded-2xl border border-[color:var(--panel-border)] bg-[color:var(--panel-bg)] p-4 hover:border-[color:var(--border-strong)] hover:bg-[color:var(--panel-alt)]"
              >
                <div className="flex flex-wrap items-center justify-between gap-2">
                  <p className="text-sm font-semibold text-[color:var(--text-primary)]">{dataset.name}</p>
                  <Badge variant="secondary">{dataset.datasetType}</Badge>
                </div>
                <p className="mt-1 text-xs text-[color:var(--text-muted)]">{dataset.description || 'No description'}</p>
                <div className="mt-2 flex flex-wrap gap-2 text-xs">
                  {dataset.tags.map((tag) => (
                    <Badge key={tag} variant="secondary">#{tag}</Badge>
                  ))}
                  <Badge variant="secondary">{dataset.columns.length} cols</Badge>
                  <Badge variant="secondary">Updated {new Date(dataset.updatedAt).toLocaleString()}</Badge>
                </div>
              </Link>
            </li>
          ))}
        </ul>
      </section>

      <Dialog open={createOpen} onOpenChange={handleCreateOpenChange}>
        <DialogContent className="max-h-[90vh] overflow-y-auto sm:max-w-3xl">
          <DialogHeader>  
            <DialogTitle>Create dataset</DialogTitle>
          </DialogHeader>

          <div className="grid gap-4 md:grid-cols-2">
            <div className="space-y-1">
              <Label>Name</Label>
              <Input
                value={createState.name}
                onChange={(event) => setCreateState((current) => ({ ...current, name: event.target.value }))}
                placeholder="Dataset name"
              />
              {fieldErrors.name ? <p className="text-xs text-rose-600">{fieldErrors.name}</p> : null}
            </div>
            <div className="space-y-1">
              <Label>Type</Label>
              <Select
                value={createState.datasetType}
                onChange={(event) => {
                  const nextType = (event.target.value as DatasetType) || 'TABLE';
                  const nextConnectionId =
                    nextType === 'TABLE' && !tableConnectors.some((connector) => connector.id === createState.connectionId)
                      ? (tableConnectors[0]?.id || '')
                      : createState.connectionId;
                  setCreateState((current) => ({
                    ...current,
                    datasetType: nextType,
                    connectionId: nextConnectionId,
                    schemaName: '',
                    tableName: '',
                  }));
                  setSchemas([]);
                  setTables([]);
                  setColumns([]);
                  setSelectedColumns({});
                  if (nextType === 'TABLE' && nextConnectionId) {
                    void loadSchemas(nextConnectionId);
                  }
                }}
              >
                <option value="TABLE">Table</option>
                <option value="SQL">SQL</option>
                <option value="FILE">File</option>
              </Select>
            </div>
            <div className="space-y-1 md:col-span-2">
              <Label>Description</Label>
              <Textarea
                rows={3}
                value={createState.description}
                onChange={(event) => setCreateState((current) => ({ ...current, description: event.target.value }))}
              />
            </div>
            <div className="space-y-1 md:col-span-2">
              <Label>Tags (comma separated)</Label>
              <Input
                value={createState.tags}
                onChange={(event) => setCreateState((current) => ({ ...current, tags: event.target.value }))}
              />
            </div>
            {
              createState.datasetType === 'TABLE' && availableCreateConnectors.length > 0 ? 
              (
                <><div className="space-y-1">
                    <Label>Connection</Label>
              <Select
                value={createState.connectionId}
                onChange={(event) => {
                        const connectionId = event.target.value;
                        setCreateState((current) => ({
                          ...current,
                          connectionId,
                          schemaName: '',
                          tableName: '',
                        }));
                        setSchemas([]);
                        setTables([]);
                        setColumns([]);
                        if (connectionId) {
                          void loadSchemas(connectionId);
                        }
                      } }
                    >
                      <option value="">Select connection</option>
                      {availableCreateConnectors.map((connector) => (
                        <option key={connector.id} value={connector.id}>
                          {connector.name}
                        </option>
                ))}
              </Select>
              {fieldErrors.connectionId ? <p className="text-xs text-rose-600">{fieldErrors.connectionId}</p> : null}
              {createState.datasetType === 'TABLE' && availableCreateConnectors.length === 0 ? (
                <p className="text-xs text-[color:var(--text-muted)]">No SQL connectors are available for table datasets.</p>
              ) : null}
                  </div><div className="space-y-1">
                      <Label>Max preview rows</Label>
                      <Input
                        value={createState.maxRowsPreview}
                        onChange={(event) => setCreateState((current) => ({ ...current, maxRowsPreview: event.target.value }))} />
                    </div></>
              )
              :
              null
            }
          </div>
          <div className="space-y-1 md:col-span-2">
            <Label>Dataset type details</Label>
          </div>
          {createState.datasetType === 'TABLE' ? (
            <div className="space-y-4 rounded-xl border border-[color:var(--panel-border)] bg-[color:var(--panel-alt)] p-4">
              <div className="grid gap-4 md:grid-cols-2">
                <div className="space-y-1">
                  <Label>Schema</Label>
                  <Select
                    value={createState.schemaName}
                    onChange={(event) => {
                      const schemaName = event.target.value;
                      setCreateState((current) => ({ ...current, schemaName, tableName: '' }));
                      setTables([]);
                      setColumns([]);
                      if (createState.connectionId && schemaName) {
                        void loadTables(createState.connectionId, schemaName);
                      }
                    }}
                  >
                    <option value="">Select schema</option>
                    {schemas.map((schema) => (
                      <option key={schema} value={schema}>
                        {schema}
                      </option>
                    ))}
                  </Select>
                  {loadingSchemas ? <p className="text-xs text-[color:var(--text-muted)]">Loading schemas...</p> : null}
                  {fieldErrors.schemaName ? <p className="text-xs text-rose-600">{fieldErrors.schemaName}</p> : null}
                </div>
                <div className="space-y-1">
                  <Label>Table</Label>
                  <Select
                    value={createState.tableName}
                    onChange={(event) => {
                      const tableName = event.target.value;
                      setCreateState((current) => ({ ...current, tableName }));
                      if (createState.connectionId && createState.schemaName && tableName) {
                        void loadColumns(createState.connectionId, createState.schemaName, tableName);
                      }
                    }}
                  >
                    <option value="">Select table</option>
                    {tables.map((table) => (
                      <option key={table} value={table}>
                        {table}
                      </option>
                    ))}
                  </Select>
                  {loadingTables ? <p className="text-xs text-[color:var(--text-muted)]">Loading tables...</p> : null}
                  {fieldErrors.tableName ? <p className="text-xs text-rose-600">{fieldErrors.tableName}</p> : null}
                </div>
              </div>

              <div className="space-y-2">
                <p className="text-sm font-medium text-[color:var(--text-primary)]">Columns ({selectedColumnCount}/{columns.length})</p>
                <div className="max-h-44 space-y-1 overflow-y-auto rounded-lg border border-[color:var(--panel-border)] bg-[color:var(--panel-bg)] p-2">
                  {loadingColumns ? <p className="text-xs text-[color:var(--text-muted)]">Loading columns...</p> : null}
                  {!loadingColumns && columns.length === 0 ? (
                    <p className="text-xs text-[color:var(--text-muted)]">Select a table to load columns.</p>
                  ) : null}
                  {columns.map((column) => (
                    <label key={column.name} className="flex items-center justify-between gap-2 rounded px-2 py-1 text-xs hover:bg-[color:var(--panel-alt)]">
                      <span className="truncate">{column.name}</span>
                      <span className="text-[color:var(--text-muted)]">{column.type}</span>
                      <input
                        type="checkbox"
                        checked={Boolean(selectedColumns[column.name])}
                        onChange={(event) =>
                          setSelectedColumns((current) => ({
                            ...current,
                            [column.name]: event.target.checked,
                          }))
                        }
                      />
                    </label>
                  ))}
                </div>
              </div>
            </div>
          ) : createState.datasetType === 'FILE' ? (
            <div className="space-y-4 rounded-xl border border-[color:var(--panel-border)] bg-[color:var(--panel-alt)] p-4">
              <div className="space-y-1">
                <Label>CSV file</Label>
                <Input
                  type="file"
                  accept=".csv,text/csv"
                  onChange={(event) =>
                    setCreateState((current) => ({
                      ...current,
                      file: event.target.files?.[0] ?? null,
                    }))
                  }
                />
                {fieldErrors.file ? <p className="text-xs text-rose-600">{fieldErrors.file}</p> : null}
                <p className="text-xs text-[color:var(--text-muted)]">
                  Upload a CSV and Langbridge will convert it to Parquet before publishing the dataset.
                </p>
              </div>
            </div>
          ) : (
            <div className="space-y-4 rounded-xl border border-[color:var(--panel-border)] bg-[color:var(--panel-alt)] p-4">
              <div className="space-y-1">
                <Label>SQL dialect</Label>
                <Select
                  value={createState.dialect}
                  onChange={(event) => setCreateState((current) => ({ ...current, dialect: event.target.value }))}
                >
                  <option value="tsql">T-SQL</option>
                  <option value="postgres">PostgreSQL</option>
                  <option value="mysql">MySQL</option>
                  <option value="snowflake">Snowflake</option>
                  <option value="redshift">Redshift</option>
                  <option value="bigquery">BigQuery</option>
                  <option value="oracle">Oracle</option>
                  <option value="sqlite">SQLite</option>
                </Select>
              </div>
              <div className="space-y-1">
                <Label>SQL text</Label>
                <Textarea
                  rows={8}
                  value={createState.sqlText}
                  onChange={(event) => setCreateState((current) => ({ ...current, sqlText: event.target.value }))}
                  placeholder="SELECT * FROM schema.table"
                  className="font-mono text-xs"
                />
                {fieldErrors.sqlText ? <p className="text-xs text-rose-600">{fieldErrors.sqlText}</p> : null}
              </div>
            </div>
          )}

          {modalError ? <ErrorPanel {...modalError} /> : null}

          <DialogFooter>
            <Button variant="outline" onClick={closeCreateModal} disabled={createMutation.isPending || uploadMutation.isPending}>
              Cancel
            </Button>
            <Button onClick={() => void submitCreate()} isLoading={createMutation.isPending || uploadMutation.isPending}>
              Create dataset
            </Button>
          </DialogFooter>
        </DialogContent>
      </Dialog>
    </div>
  );
}
